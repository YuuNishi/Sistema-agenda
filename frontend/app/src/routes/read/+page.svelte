<h1>Questões do back do projeto</h1>
<ul>
    <li>Use o editor de sua preferência como o PyCharm ou o VSCode </li>
    <li>Este projeto usa o Python 3.12</li>
    <li>Certifique-se de instalar o FastApi e o uvicorn</li>
    <li>Para rodar o projeto é necessário estar com todas as dependências instaladas, para isso rode: pip install -r requirements.txt</li>
    <li>Para executar é só executar o comando: uvicorn main:app --reload na pasta API</li>
    <li>Para testar os endpoints no swagger rode a aplicação e vá para /docs no seu localhost </li>
</ul>
